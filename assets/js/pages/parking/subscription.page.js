parasails.registerPage('subscriptions', {
  //  ╦╔╗╔╦╔╦╗╦╔═╗╦    ╔═╗╔╦╗╔═╗╔╦╗╔═╗
  //  ║║║║║ ║ ║╠═╣║    ╚═╗ ║ ╠═╣ ║ ║╣
  //  ╩╝╚╝╩ ╩ ╩╩ ╩╩═╝  ╚═╝ ╩ ╩ ╩ ╩ ╚═╝
  data: {
    monthName:'',
    monthData: [],
    subscriptions: {}
  },

  //  ╦  ╦╔═╗╔═╗╔═╗╦ ╦╔═╗╦  ╔═╗
  //  ║  ║╠╣ ║╣ ║  ╚╦╝║  ║  ║╣
  //  ╩═╝╩╚  ╚═╝╚═╝ ╩ ╚═╝╩═╝╚═╝
  beforeMount: async function() {
    // Attach raw data exposed by the server.
    _.extend(this, SAILS_LOCALS);
    this.subscriptions = await Cloud.subscriptions();
    this.monthName = this.subscriptions.monthName;
    this.monthData = this.subscriptions.monthData;

  },
  mounted: async function() {
    //…
  },

  //  ╦╔╗╔╔╦╗╔═╗╦═╗╔═╗╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
  //  ║║║║ ║ ║╣ ╠╦╝╠═╣║   ║ ║║ ║║║║╚═╗
  //  ╩╝╚╝ ╩ ╚═╝╩╚═╩ ╩╚═╝ ╩ ╩╚═╝╝╚╝╚═╝
  methods: {

    toggleSuscription: async function(date){
      await Cloud.toggleSubscription.with({date: date});
      this.updateMonth();
    },

    updateMonth: async function(){
      this.subscriptions = await Cloud.subscriptions();
      this.monthName = this.subscriptions.monthName;
      this.monthData = this.subscriptions.monthData;
    }
  }
});
